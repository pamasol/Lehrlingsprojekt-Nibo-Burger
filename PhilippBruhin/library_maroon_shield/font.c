uint8_t font_load_glyph5(uint16_t index, uint8_t * pxbuf, uint8_t mode) {
  uint8_t result;
  index *= 6;
  memcpy_P(pxbuf, fontTiles5+index+1, 5);
  if (mode==FONT_PROP_LEFT) {
    result=0;
    uint8_t mask = pgm_read_byte (fontTiles5+index);
    uint8_t * pxbuf2 = pxbuf;
    while (mask) {
      if (mask & 0x01) {
        *pxbuf++ = *pxbuf2++;
        result++;
      } else {
        pxbuf2++;
      }
      mask >>= 1;
    }
  } else {
    result = 5;
  }
  return result;
}


uint8_t font_load_glyph8(uint16_t index, uint8_t * pxbuf) {
  index *= 8;
  memcpy_P(pxbuf, fontTiles8+index, 8);
  return 8;
}


uint8_t font_load_glyph3_ascii(uint16_t index, uint8_t * pxbuf) {
  index *= 3;
  const uint8_t * ptr = fontTiles3ASCII+index;  
  *pxbuf++ = pgm_read_byte_near(ptr++);
  *pxbuf++ = pgm_read_byte_near(ptr++);
  *pxbuf++ = pgm_read_byte_near(ptr++);
  return 3;
}

